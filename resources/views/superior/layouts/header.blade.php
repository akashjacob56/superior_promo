$("#searchbar-view").addClass("hidden");
$("#searchbar-view").empty();

}else{

form_data = new FormData();

form_data.append('keyword',keyword);
form_data.append( "_token", "{{ csrf_token() }}" );

$.ajaxSetup
({
headers : {'X-CSRF-TOKEN' : $('meta[name="csrf-token"]').attr('content')}
});


$.ajax({
method: "POST",
url: "{{$base_url}}/searchbar-view",
dataType: 'json',
cache: false,
contentType: false,
processData: false,
data: form_data,

success: function(data){

$("#searchbar-view").removeClass("hidden");

document.getElementById("searchbar-view").innerHTML=data.data.data;

},

error: function (xhr, textStatus, errorThrown) {
alert(textStatus + ':' + errorThrown); 
} 

});

}

});
});


